NS_LOG_COMPONENT_DEFINE("FakeBsSimulation");

// LTE Timing Advance step: c * 16 * Ts / 2, Ts = 1/30.72e6 s
static const double LTE_TA_STEP_M = 78.125;

// Fixed PLMN for this simulation (MCC=001, MNC=01)
static const uint32_t SIM_MCC = 1;
static const uint32_t SIM_MNC = 1;

// =============================================================================
// Log-normal shadowing noise
//
// Each RSRP sample is corrupted by an independent Gaussian draw:
//   rsrp_noisy = rsrp_clean + N(0, sigma_sh^2)
//
// sigma_sh is set by --shadowingStd (default 8 dB per COST-231 urban macro).
// Set to 0 to disable noise (noiseless, deterministic simulation).
// =============================================================================
static double             g_shadowingStdDb = 8.0;
static uint32_t           g_shadowingSeed  = 1;
static std::mt19937       g_shadowRng;
static std::normal_distribution<double> g_shadowDist{0.0, 1.0};

// Maximum number of cells per UE measurement report (models 3GPP maxReportCells IE).
// In real LTE, UE reports at most N strongest cells. 0 = no limit (report all).
static uint32_t           g_maxReportCells = 5;

// =============================================================================
// MeasReportConfig — models the RRC Measurement Configuration sent by eNB
//
// In real LTE, eNB sends RRCConnectionReconfiguration → measConfig which
// contains:
//   measObjectEUTRA : frequency + cell list to measure
//   reportConfig    : what to report (RSRP/RSRQ) + trigger (periodic/event)
//   cellIdReporting : when true, UE includes ECGI of each reported cell
//   reportInterval  : periodic reporting interval
//
// This struct captures the relevant parameters; PeriodicMeasure() uses it
// to generate measurement reports that include ECGI for every visible cell.
// =============================================================================
struct MeasReportConfig
{
    bool   cellIdReporting; // true → UE includes ECGI in measurement report
    double intervalSec;     // reporting interval [s] (mapped from reportInterval IE)
};
static MeasReportConfig g_measReportCfg;

// =============================================================================
// CellRecord
// =============================================================================
struct CellRecord
{
    uint16_t cellId;        // ns-3 internal ID (unique per device)
    EcgiT    ecgi;          // ECGI (globally unique; fake shares one legal ECGI)
    double   posX;
    double   posY;
    double   posZ;          // antenna height [m]
    double   txPowerDbm;
    bool     isFake;
};

// =============================================================================
// GlobalCellDatabase — singleton
// =============================================================================
class GlobalCellDatabase
{
public:
    static GlobalCellDatabase& Instance()
    {
        static GlobalCellDatabase inst;
        return inst;
    }

    void RegisterCell(uint16_t cellId, EcgiT ecgi,
                      double posX, double posY, double posZ,
                      double txPowerDbm, bool isFake)
    {
        m_cells[cellId] = CellRecord{cellId, ecgi, posX, posY, posZ, txPowerDbm, isFake};
        SIM_LOG(DEBUG, "GlobalCellDB",
                "registered cellId=" << cellId
                << " ECGI=" << EcgiToStr(ecgi)
                << (isFake ? " [FAKE]" : " [legal]")
                << " pos=(" << posX << "," << posY << "," << posZ << ")"
                << " txPower=" << txPowerDbm << "dBm");
    }

    // All cells (legal + fake)
    // Columns: ecgi, pos_x, pos_y, tx_power_dbm, is_fake
    void ExportCsv(const std::string& path) const
    {
        std::ofstream ofs(path);
        if (!ofs.is_open())
        {
            SIM_LOG(ERR, "GlobalCellDB",
                    "Cannot open cell_database CSV: " << path);
            return;
        }
        ofs << "ecgi,pos_x,pos_y,pos_z,tx_power_dbm,is_fake\n";
        for (const auto& kv : m_cells)
        {
            const CellRecord& r = kv.second;
            ofs << EcgiToStr(r.ecgi) << ","
                << std::fixed << std::setprecision(3)
                << r.posX       << ","
                << r.posY       << ","
                << r.posZ       << ","
                << r.txPowerDbm << ","
                << (r.isFake ? 1 : 0) << "\n";
        }
        SIM_LOG(INFO, "GlobalCellDB",
                "Cell database exported → " << path
                << "  (" << m_cells.size() << " cells)");
    }

    // Legal cells only
    // Columns: ecgi, pos_x, pos_y, tx_power_dbm
    void ExportLegalMapCsv(const std::string& path) const
    {
        std::ofstream ofs(path);
        if (!ofs.is_open())
        {
            SIM_LOG(ERR, "GlobalCellDB",
                    "Cannot open legal_cell_map CSV: " << path);
            return;
        }
        ofs << "ecgi,pos_x,pos_y,pos_z,tx_power_dbm\n";
        uint32_t count = 0;
        for (const auto& kv : m_cells)
        {
            const CellRecord& r = kv.second;
            if (r.isFake) continue;
            ofs << EcgiToStr(r.ecgi) << ","
                << std::fixed << std::setprecision(3)
                << r.posX       << ","
                << r.posY       << ","
                << r.posZ       << ","
                << r.txPowerDbm << "\n";
            ++count;
        }
        SIM_LOG(INFO, "GlobalCellDB",
                "Legal cell map exported → " << path
                << "  (" << count << " cells)");
    }

    const std::map<uint16_t, CellRecord>& GetCells() const { return m_cells; }

private:
    GlobalCellDatabase() = default;
    std::map<uint16_t, CellRecord> m_cells; // cellId → CellRecord
};

// =============================================================================
// MeasurementRecord — one row in measurements.csv
//
// One record per (UE, ECGI) per interval.
// cell_role: S = serving (strongest ECGI), N = neighbour
// Includes ECGI as configured by eNB via MeasReportConfig.cellIdReporting.
// =============================================================================
struct MeasurementRecord
{
    double   timeSec;
    uint64_t imsi;
    double   ueX;
    double   ueY;
    double   ueZ;
    uint16_t cellId;      // ns-3 unique cell ID (distinguishes fake vs legal even with same ECGI)
    EcgiT    ecgi;
    bool     isServing;
    double   rsrpDbm;
    int      timingAdvance;
};

// =============================================================================
// GlobalMeasurementCollector — singleton
// =============================================================================
class GlobalMeasurementCollector
{
public:
    static GlobalMeasurementCollector& Instance()
    {
        static GlobalMeasurementCollector inst;
        return inst;
    }

    void Open(const std::string& path)
    {
        m_ofs.open(path);
        if (!m_ofs.is_open())
        {
            SIM_LOG(ERR, "MeasureColl",
                    "Cannot open measurements CSV: " << path);
            return;
        }
        // cell_id: unique ns-3 cell ID (fake and legal have different cell_id even with same ECGI)
        // ecgi column included per MeasReportConfig.cellIdReporting = true
        m_ofs << "time_sec,imsi,ue_x,ue_y,ue_z,cell_id,ecgi,cell_role,rsrp_dbm,timing_advance\n";
        SIM_LOG(INFO, "MeasureColl", "Measurements CSV opened: " << path);
    }

    // Open separate CSV for UEs whose serving cell is the fake BS
    void OpenFakeServed(const std::string& path)
    {
        m_fakeOfs.open(path);
        if (!m_fakeOfs.is_open())
        {
            SIM_LOG(ERR, "MeasureColl",
                    "Cannot open fake_served_ues CSV: " << path);
            return;
        }
        m_fakeOfs << "time_sec,imsi,ue_x,ue_y,ue_z,serving_ecgi,rsrp_dbm,timing_advance\n";
        SIM_LOG(INFO, "MeasureColl", "Fake-served UEs CSV opened: " << path);
    }

    void AddMeasurement(const MeasurementRecord& rec)
    {
        if (!m_ofs.is_open()) return;
        m_ofs << std::fixed << std::setprecision(3)
              << rec.timeSec                 << ","
              << rec.imsi                    << ","
              << rec.ueX                     << ","
              << rec.ueY                     << ","
              << rec.ueZ                     << ","
              << rec.cellId                  << ","
              << EcgiToStr(rec.ecgi)         << ","
              << (rec.isServing ? "S" : "N") << ","
              << rec.rsrpDbm                 << ","
              << rec.timingAdvance           << "\n";
        m_ofs.flush();
    }

    // Write one row for a UE currently camped on the fake BS
    void AddFakeServed(double timeSec, uint64_t imsi,
                       double ueX, double ueY, double ueZ,
                       EcgiT servEcgi, double rsrpDbm, int ta)
    {
        if (!m_fakeOfs.is_open()) return;
        m_fakeOfs << std::fixed << std::setprecision(3)
                  << timeSec              << ","
                  << imsi                 << ","
                  << ueX                  << ","
                  << ueY                  << ","
                  << ueZ                  << ","
                  << EcgiToStr(servEcgi)  << ","
                  << rsrpDbm              << ","
                  << ta                   << "\n";
        m_fakeOfs.flush();
    }

    void Close()
    {
        if (m_ofs.is_open())     m_ofs.close();
        if (m_fakeOfs.is_open()) m_fakeOfs.close();
    }

private:
    GlobalMeasurementCollector() = default;
    std::ofstream m_ofs;
    std::ofstream m_fakeOfs;
};

