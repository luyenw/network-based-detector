/* =============================================================================
 * fake-bs-sim.cc
 *
 * Physics-Constrained Cooperative Detection and Localization of
 * Fake Base Stations Using Timing Advance
 *
 * ns-3.39 LTE simulation:
 *   - Grid of legal eNBs (gridSize x gridSize)
 *   - One fake eNB configured with the same ECGI as one legal cell (--fakeEci)
 *   - UEs with random-waypoint mobility
 *   - 1-second periodic measurement report as configured by eNB (MeasReportConfig)
 *
 * ECGI-based measurement model:
 *   In real LTE, eNB sends RRCConnectionReconfiguration containing a
 *   MeasurementConfiguration IE that instructs the UE to:
 *     - Measure RSRP/RSRQ for cells specified in measObjectEUTRA
 *     - Include the ECGI of each reported cell (cellIdReporting = enabled)
 *     - Report periodically at the configured interval
 *   This simulation models that configuration via MeasReportConfig.
 *
 *   When fake and legal share an ECGI, the UE sees the stronger signal (max
 *   RSRP) at that ECGI.  CC-PCV compares against the known LEGAL position →
 *   anomalous residual → detection.
 *
 * Outputs (outputDir/):
 *   simulation.log      — full structured log (DEBUG level)
 *   measurements.csv    — time_sec, imsi, ue_x, ue_y, ecgi,
 *                         cell_role (S/N), rsrp_dbm, timing_advance
 *   cell_database.csv   — all cells: ecgi, pos_x, pos_y, tx_power_dbm, is_fake
 *   legal_cell_map.csv  — legal cells only: ecgi, pos_x, pos_y, tx_power_dbm
 *   ccpcv_results.csv   — time_sec, ecgi, residual, status
 *
 * TA NOTE:
 *   ns-3.39 has no TimingAdvance trace source on LteUeRrc.
 *   TA derived geometrically: TA = round(dist_m / 78.125)
 *
 *   LteUePhy::ReportCurrentCellRsrpSinr never fires in RRC_CONNECTED state.
 *   Measurements generated by PeriodicMeasure() via geometric path-loss.
 *
 * Run:
 *   ./ns3 run "fake-bs-sim --gridSize=3 --numUe=20 --simTime=30 --fakeEci=1"
 * =============================================================================
 */

